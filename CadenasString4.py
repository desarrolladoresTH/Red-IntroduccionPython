#cadena de caracteres
palabra = "Python"

#Tamaño de la cadena(len)
print(len(palabra))

print(" ")

#Permite acceder a caracteres individuales de la cadena de texto(Indexing)
print(palabra[0])   
print(palabra[1])  
print(palabra[2])  
print(palabra[3])  
print(palabra[4])  
print(palabra[5])  

#Obtener una rebanada (porción) de una cadena de caracteres(Slicing)
print(palabra[1:4])

#inicio
print(palabra[0:])
#final
print(palabra[:6]) 
#copia
print(palabra[:])


#pasos
print(palabra [1:6:2])